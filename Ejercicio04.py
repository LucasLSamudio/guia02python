# - Leer el siguiente programa. ¿Para qué valores de la variable a imprime "hola!" cuando se
#   ejecuta? ¿Y para cuáles "chau!"?

a = int(input("Ingrese un valor para a"))
if a == 0 :
    print("hola!")
else:
    print("chau!")

# Se imprime "hola!" cuando el valor ingresado asignado a la variable a es 0, cualquier otro valor ingresado
# daría como resultado que se ejecute el else.